package day01;

// 이름(내용 있어도 되고 없어도 되고) -> method, 메서드
// 메서드 -> 자연어의 동사에 해당

public class hello3 {
                      // main 메서드
	                  // : 프로그램의 시작점
	                  // : 프로그램 내부에 main 메서드는 유일해야함.
	public static void main(String[] args) {
		
		System.out.println("Hello JAVA!!!");
		             // println 메서드
		             // 메서드를 사용하는 것보다는 메서드를 호출(call)한다라고 표현.
		System.out.println("Hello JAVA!!!");
	}

}
