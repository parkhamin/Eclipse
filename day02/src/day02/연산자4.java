package day02;

public class 연산자4 {

	public static void main(String[] args) {
		// 비교연산자
		// > < >= <= == !=
		// 피연산자는 숫자, 연산의 결과는 논리값(true/false)
		
		System.out.println( 3 < 4 );
		System.out.println( 3 > 4 );
		System.out.println( 3 <= 4 );
		System.out.println( 3 >= 4 );
		System.out.println( 3 == 4 ); // equal
		System.out.println( 3 != 4 ); // not equal
	}

}
