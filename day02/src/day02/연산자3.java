package day02;

public class 연산자3 {

	public static void main(String[] args) {
		// 복합대입연산자
		// += -+ *= /= %=
		
		int a = 10;
		
		a += 1; // a = a + 1;
		System.out.println(a);
		a -= 2; // a = a -2;
		System.out.println(a);
		a *= 3;
		System.out.println(a);
		a /= 4;
		System.out.println(a);
		a %= 5;
		System.out.println(a);

	}

}
