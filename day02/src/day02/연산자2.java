package day02;

public class 연산자2 {

	public static void main(String[] args) {
		// 연산을 하려면 피연산자의 타입이 같아야 함.
		
		// 산술연산자
		// + - * / %
		System.out.println( 10 + 3 );
		System.out.println( 10 - 3 );
		System.out.println( 10 * 3 );
		System.out.println( 10 / 3 );
		System.out.println( 10 % 3 );
		// % -> mod 연산 (나머지를 구하는 연산)
	}

}
