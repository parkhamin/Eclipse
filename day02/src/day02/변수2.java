package day02;

public class 변수2 {

	public static void main(String[] args) {
		
		int a; // 변수를 만드는 것 -> "변수를 선언(declare)한다."라고 표현
		a = 1; // 변수에 값을 저장하는 것 -> 변수에 값을 할당(대입)한다.
		// 변수에 값을 최초로 저장하는 것 -> 변수를 초기화(initialize)한다.
		
		int b = 1; // 변수를 선언과 동시에 초기화하는 것도 가능.
		int c, d, e; // 같은 데이터타입에 변수는 여러 개를 동시에 선언 가능.
		int f = 1, g = 2, h;
	}

}
