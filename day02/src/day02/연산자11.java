package day02;

public class 연산자11 {

	public static void main(String[] args) {
		
		System.out.println(1 + 2);
		
		System.out.println("apple" + "banana");
		// +의 피연산자가 문지열인 경우, 문자열 결합연산자로 사용됨
		
		System.out.println('a' + 'b'); // 산술연산자
		
		System.out.println(1+ "2"); // 문자열 결합연산자
		// 문자열과 숫자를 + 하려고 하면 컴파일러가 숫자를 문자열로 변환함

	}

}
